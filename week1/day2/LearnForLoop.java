package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {

		//for- ctrl+space
		
		//i=0-start point
		//i< ,i>,i<=,i>= - condition
		//i++ -increment by 1 / i-- -decrement 
		for (int i = 0; i<10 ; i++) {
			System.out.println(i);
		}
		
		System.out.println("-----------------------------");
		
		for (int i = 50; i >=0; i--) {
			System.out.println(i);
		}
		
	}

}
